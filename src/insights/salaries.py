from typing import Union, List, Dict
from src.insights.jobs import read


def get_max_salary(path: str) -> int:
    """Get the maximum salary of all jobs

    Must call `read`

    Parameters
    ----------
    path : str
        Must be passed to `read`

    Returns
    -------
    int
        The maximum salary paid out of all job opportunities
    """
    data = read(path)
    max_salary = None
    # para cada linha de emprego nos dados carregados
    for row in data:
        # obtém o valor de max_salary da linha, caso exista
        salary = row.get("max_salary")
        # verifica se o valor de max_salary é diferente de None
        if salary:
            # tenta converter o valor de max_salary para um inteiro
            try:
                salary = int(salary)
                """ se max_salary ainda não tiver definido ou se
                o novo valor for maior que o anterior, atualiza max_salary"""
                if max_salary is None or salary > max_salary:
                    max_salary = salary
                """ ignora exceções de ValueError, q pode ser lançadas
                caso max_salary não possa ser convertido para inteiro """
            except ValueError:
                pass
    return max_salary
